const mongoose = require('mongoose')
const Schema = mongoose.Schema;

const ManageSchema = new Schema({
    employername: {
        type:String,
        require: true

    },
    designation:{
        type:String,
        require: true
    },
    email:{
        type:String,
        require: true
    },
    phonenumber: {
        type: String,
        require: true
    }

})

let manageData = mongoose.model('signupcredential', ManageSchema);

module.exports = manageData